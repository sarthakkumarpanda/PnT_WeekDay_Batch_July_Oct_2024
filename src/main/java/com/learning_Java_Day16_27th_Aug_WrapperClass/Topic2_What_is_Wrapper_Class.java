package com.learning_Java_Day16_27th_Aug_WrapperClass;

public class Topic2_What_is_Wrapper_Class {
	//int - Integer
	//long - Long
	//short - Short
	//boolean - Boolean
	//double - Double
	//char - Character
	//float - Float
	//byte - Byte
	
	
	//Wrapper classes are pre-defined classes which can contain primitive data type
	//They wrap primitive data types to Wrapper Class Object

	public static void main(String[] args) {
		
		int a = 10;
		System.out.println("The primitive data type value is : " + a);
	
		
		Integer b = 10;
		System.out.println("The Wrapper class object value is : " + b);
		
	

	}

}
