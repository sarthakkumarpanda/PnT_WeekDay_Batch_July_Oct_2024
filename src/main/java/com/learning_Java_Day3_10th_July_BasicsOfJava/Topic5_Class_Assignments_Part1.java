package com.learning_Java_Day3_10th_July_BasicsOfJava;

public class Topic5_Class_Assignments_Part1 {

	public static void main(String[] args) {
		
		
		short s1 = 100;
		byte b1 = 25;
		int i = 10;
		long l = 1000000;
		
		System.out.println(s1 + b1 + i + l);
		
		char c1 = '1';
		
		System.out.println(i + c1);  //ASCII value
		//American Standard Code of Information Interchange
		
		String s2 = "Hello";
		
		System.out.println(s2 + c1);
		
		boolean b2 = true;
		System.out.println(b2 + s2);
		
		

	}

}
