package com.learning_Java_Day12_13th_Aug_OOPs_Methods;

public class Topic1_Introduction_to_Methods {
	
	//Methods are independent units of execution
	//How many types of methods are there?
	//1. Concrete Method
	//2. Unimplemented Method - a method which does not have a body
	
	//Methods are also called functions

	public static void main(String[] args) {
		
		//main method is the execution center of java programming
		logicStarts();
		logicMidway();
		logicEnds();
	}
	
	public static void logicStarts() {
		System.out.println("this is where the logic begins");
	}
	
	public static void logicMidway() {
		System.out.println("this is where the logic is in middle");
	}
	
	public static void logicEnds() {
		System.out.println("this is where the logic ends");
	}
	
	
	

}
