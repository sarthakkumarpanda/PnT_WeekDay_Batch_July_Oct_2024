package com.learning_Java_Day13_14th_Aug_Inheritance;

public class Topic1_what_is_inheritance {
	
	//inheritance is inheriting properties of parent class into child class
	//inheritance is a mechanism in which one object(child) acquires properties and behavior of parent object
	//inheritance represents IS-A relationship which is also known as parent-child relationship
	
	//Dog is an Animal
	//Car is a Vehicle
	//Surgeon is a Doctor
	
	//What are the advantages of inheritance
	//1. Code re-usability
	//2. It promotes RunTime polymorpshim by allowing method overriding
	
	//Disadvantages:
	//1. The parent and child classes get tightly coupled
	
	//How to achieve inheritance ?
	//Answer: extends keyword
	
	//Which entities do not take part in inheritance?
	//1. Constructors
	//2. private members
	
	//java does not support multiple inheritance
	//Parent class is known as super class
	//Child class is known as sub class
	
	//Levels of inheritance in java
	//1. Single inheritance - One child extends one parent class
	//2. Multilevel inheritance - child extends Parent, Parent extends GrandParent
	//3. Hierarchical inheritance - One parent is extended by multiple child Classes

}
